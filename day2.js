const figurePathPart2 = (directions) => {
  const positions = { depth: 0, horizontal: 0, aim: 0}

  const directionTypes = {
      up: (num) => positions.aim -= Number(num),

      down: (num) => positions.aim += Number(num),

      forward: (num) => {
        positions.horizontal += Number(num)

        if (positions.aim > 0) {
            positions.depth += Number(num) * positions.aim
        }
      }
  }

  for (let direction of directions) {
    const whereTo = direction.split(" ")
    const [type, length] = whereTo;
    directionTypes[type](length)
}

  return  positions.horizontal * positions.depth;
}

